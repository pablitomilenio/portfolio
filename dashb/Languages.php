<h1>Welche Programmiersprachen gibt es, und was kann man damit machen?</h1>

<br><br>
ABAP: Die Programmiersprache für SAP. 'Systeme Anwendungen und Programme - SAP'. Die meisten IT-Firmen wie Google oder Amazon stammen aus USA. Von den IT-Weltkonzernen stammt nur SAP aus Deutschland.
Abap personalisisert und automatisiert SAP so dass es den Kundenwünschen gerecht wird. Die Umtransformierung vom P15 zum FGSD-Ticketsystem hat vermutlich in ABAP stattgefunden.
Da SAP ein Programm von Großkonzernen ist und die wichtigsten Unternehmensfunktionen abbildet, verdienen SAP Programmierer unter den Programmierern vermutlich am Meisten.
<br><br>
So wie die gesprochenen Sprachen Ähnlichkeiten untereinander haben, so sind viele Programmierkonstrukte, sehr viele sogar, von Sprache zu Sprache ähnlich. Die Zeichensetzung (Syntax) ist etwas anders
Die Sprache der Funktionsaufrufe ist meistens Englisch, aber es sind immer nur kurze Wörter, man muss also nicht Englisch sprechen können um zu programmieren. Die Hauptunterscheidung sind meistens
die Bibliotheken auf die zugegriffen wird und aus denen die Funktionalität entspringt.
<br><br>
Um die Bibliotheken zu nutzen, bedarf es eines Grundverständnisses des Sachgebiets. um 2d zu programmieren benötit man Pytagoras, Liniensteigung, Kreiswinkel, etc. Um den Arbeitssspeicher von Windows
anzusprechen bedarf es der Kenntnisse wie der Arbeitsspeicher in Hexadezimal oder Binär arbeitet. Für Webseiten muss der Aufbau von Tabellen, Buttons, Farbverläufen, Hyperlinks etc verstanden werden.
Mit diesem Verständnis ruft man dann einfach die Bibliotheken auf. Z.B: <br><br> Kreis(von positionx, zu Position y, mit Radius r, farbe: hellblau)
<br><br>
Die 'C' Sprachen z.B: C# oder C++ : Das ist die Hauptsprache mit der man 'alles' programmieren kann und tief in Windows eingreifen kann. Diese Sprache ist sehr mächtig in Möglichkeiten aber
auch etwas älter und eher schwer zu lernen und zu verstehen. Eher für Diplom Informatiker gedacht die richtig tiefgehende Experten sind. Hiermit etwas farbliches oder optisch ansprechenderes
zu programmieren ist möglich, da die Sprache 'alles' kann, aber der Weg dorthin ist weit und steinig. C Programme können auch für Linux oder Android neu kompiliert werden, wenn man
Betriebssystemspezifische technische Änderungen entsprechend vornimmt. Die großen Anwendungen wie Photoshop und die Spieleklassiker wie Wolfenstein wurden in 'C' Sprachen programmiert.
<br><br>
.NET : Das ist eine Mirosoft Sprache nur für Windows. Basiert u.a. auf dem .NET Framework und nutzt Microsoft Bibliotheken. 
Mit dieser Sprache werden typischerweise Anwendungen Programmiert, mit der Hauptfarbe grau, mit einfachen Textfeldern und einer kleinen Access Datenbank die einfache Funktionen übernehmen.
Vermutlich nicht so schwer zu lernen, hauptsächlich Bibliotheksaufruf, aber die verfügbaren Bibliotheken sind meistens nicht so spannend.
<br><br>
Dann gibt es noch mathematische Sprachen die für Mathematiker gedacht sind und auf bestimmte Bereiche der Mathematik spezailisiert sind
<br><br>
Für Statistik gibt es noch das Programm / Sprache 'R'
<br><br>
HTML(5) - Kann alles was der Browser kann. Eher leicht zu lernen. 2D und 3D neuerdings möglich. Eingriffe in das Windows Betriebssystem nicht möglich. Es wäre z.B. nicht möglich, 
ein Outlook Addin in HTML5 zu prorammieren, da man für das Outlook Programm den Browser erstmal verlassen müsste. Dies ist eine Sicherheitseinschränkung die nur mit Java umgangen werden
kann. Der Broweser ist eine 'Sandbox' ein Sandkasten mit Mauern über die die Hacker aud dem Internet aus Sicherheitsgründen nicht hinaus können dürfen. 30 moderne Bibliotheken verfügbar. 
<br><br>
PHP: Serverseitige Sprache für Webseiten. Ergänzt HTML und kann so über die Sandbox hinaus. Recht ausgereift, einfach zu handhaben und umfangreich. Ermöglicht die Anbindung von Webseiten an
professionelle Datenbanken und andere Datenquellen. Zahlen und Textmanipulation, Datumshandhabung, etc. Die Programme die in PHP laufen werden auf dem Prozessor von Hochleistungsservern im Rechenzentrum
ausgeführt und liefern dem Browser nur das Ergebnis, welcher das dann in HTML an den User ausgibt.
<br><br>
Java: Hat sich zur Jahrtausendwende hoher Beliebtheit erfreut, ist mittleren Alters, hat viele interessante etwas ältere Bibliotheken. Kann nicht so viel wie C, aber dennoch recht viel. Ist nicht 
ganz einfach zu lernen. Wichtigster Punkt: Muss nicht für jedes Betriebssystem neu kompiliert werden, d.h. man programmiert einmal und kann dann sowohl auf Linux als auch auf Android etc das Programm
laufen lassen. Dies ist jedoch nur die halbe Wahrheit, weil für die Nutzung immer das Java Grundprogramm installiert sein muss. Ist eher trägfällig. HTML5 hat sich die Philosophie von Java 'kopiert'. 
Man kann heutzutage den Browser als Universalplattform auf allen Betriebssystemen nutzen.
<br><br>
Visual Basic: Sprache für Office Programme. 'Basic' wie 'einfach' soll die Schierigkeiten die es zum Beispiel bei 'C' gibt erleichtern. Ist auch wirklich nicht besonders schwer zu lernen, kann 
alles und jedes was in Office denkbar und machbar ist und ermöglicht eine weitere Anpassung und Automatisierung von Office Programmen. Die Sprache bleibt aber 'basic' und kommt über den Office Horizont 
nicht hinaus. 2d und 3d nur sehr eingeschränkt im Rahmen von Excel Diagrammen möglich. Hier ist zu beachten, dass die Office Programme zum Teil durch Webanwendungen ersetzt werden und dass es
zu Microsoft Office auch noch die kostenfreie OpenOffice Anwendung gibt. Während das Konzept ein digitales Spreadsheet und ein digitales Textverarbeitungsprogramm zu haben, am Anfang revolutionär waren, 
hat es in den letzten Jahren, bis auf einige optische Auffrischungen, keine wesentlichen Innovationen mehr gegben. Stürzt manchmal ab. Makros müssen zur Verwendung immer aktiviert werden. Kann langsam werden.
